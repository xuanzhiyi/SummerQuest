const ALL_WORDS: Record<LanguagePair, WordPair[]> = {
  english_finnish: WORDS_ENGLISH_FINNISH,
  english_chinese: WORDS_ENGLISH_CHINESE,
  english_swedish: WORDS_ENGLISH_SWEDISH,
  english_french:  WORDS_ENGLISH_FRENCH,
}

function pickRandom<T>(arr: T[], n: number): T[] {
  const shuffled = [...arr]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled.slice(0, n)
}

// Picks mostly from the target level, with a smaller share of review words
// from the level below, so kids see new vocab without losing the old.
export function getRandomWords(pair: LanguagePair, count = 5, level = 5): WordPair[] {
  const source = ALL_WORDS[pair]
  const atLevel = source.filter(w => w.level === level)
  const below = level > 1 ? source.filter(w => w.level === level - 1) : []

  const targetCount = Math.round(count * 0.8)
  const reviewCount = count - targetCount

  const picked = [
    ...pickRandom(atLevel, Math.min(targetCount, atLevel.length)),
    ...pickRandom(below, Math.min(reviewCount, below.length)),
  ]

  if (picked.length < count) {
    const pickedIds = new Set(picked.map(w => w.wordId))
    const extra = pickRandom(atLevel.filter(w => !pickedIds.has(w.wordId)), count - picked.length)
    picked.push(...extra)
  }

  return pickRandom(picked, picked.length)
}
